# Дадени вероятности за лекарите
P_A1 = 0.83
P_A2 = 0.73
P_A3 = 0.93

# a) Само един лекар поставя вярна диагноза
P_only_one = (P_A1 * (1 - P_A2) * (1 - P_A3)) + \
             ((1 - P_A1) * P_A2 * (1 - P_A3)) + \
             ((1 - P_A1) * (1 - P_A2) * P_A3)

# b) Поне един лекар поставя вярна диагноза
P_at_least_one = 1 - ((1 - P_A1) * (1 - P_A2) * (1 - P_A3))

# c) Кардиологът и точно един от другите лекари поставят вярна диагноза
P_A1_and_one_other = (P_A1 * P_A2 * (1 - P_A3)) + \
                     (P_A1 * (1 - P_A2) * P_A3)

# d) Поставени са една или две верни диагнози
P_one_or_two = P_only_one + \
               (P_A1 * P_A2 * (1 - P_A3)) + \
               (P_A1 * (1 - P_A2) * P_A3) + \
               ((1 - P_A1) * P_A2 * P_A3)

# Отпечатване на резултатите
print(f"а) Само един лекар поставя вярна диагноза: {P_only_one:.4f}")
print(f"б) Поне един лекар поставя вярна диагноза: {P_at_least_one:.4f}")
print(f"в) Кардиологът и точно един от другите лекари поставят вярна диагноза: {P_A1_and_one_other:.4f}")
print(f"г) Поставени са една или две верни диагнози: {P_one_or_two:.4f}")
